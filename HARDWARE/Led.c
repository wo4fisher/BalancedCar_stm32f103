#include "Led.h"

/**************************************************************************
函数功能：LED接口初始化
入口参数：无 
返回  值：无
**************************************************************************/
void LED_Init(void)
{
	RCC->APB2ENR|=1<<4; //使能 PORTC 时钟  
	GPIOC->CRH&=0XFF0FFFFF;
	GPIOC->CRH|=0X00300000;//PC13 推挽输出
	GPIOC->ODR|=1<<13; //PC13 输出高
}
